import java.util.Scanner;

public class cambiarTiposdeDato{
	public static void main (String [] args){
		
		//constante 
		final double PI = 3.141592; // final define constantes, y double numeros con decimales
		System.out.println("la constante de pi es: " + PI);
		
		//casting
		int piSinDecimales = (int) PI; //se declara una variable piSinDecimales del tipo entero, y se le asigna el entero de PI
		
		System.out.println("la constante pi sin decimales es " + piSinDecimales);
		
		} 
}